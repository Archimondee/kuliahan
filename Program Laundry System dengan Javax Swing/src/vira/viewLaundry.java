    public void hapusData(){
        konekDatabase();
        bersih();
        try{
            String cari = JOptionPane.showInputDialog("Masukan No Antrian");
            Statement stmt = con.createStatement();
            String sql = "DELETE FROM data_laundry WHERE no_antrian = '"+cari+"'";
            String query = "DELETE FROM detail_laundry WHERE no_antrian ='"+cari+"'";
            stmt.executeUpdate(sql);
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Terhapuskan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void cariData(){
        konekDatabase();
        try{
            String cari = JOptionPane.showInputDialog("Masukan no antrian");
            Statement stmt = con.createStatement();
            String query = "SELECT * from detail_laundry where no_antrian = '"+cari+"'";
            String sql = "SELECT * from data_laundry WHERE no_antrian like '"+cari+"'";
            ResultSet rs = stmt.executeQuery(sql);
            if(rs.next()){
                txtAntrian.setText(rs.getString(1));
                txtNama.setText(rs.getString(2));
            }else{
                JOptionPane.showMessageDialog(null, "Data tidak ada");
            }
            ResultSet rs1 = stmt.executeQuery(query);
            if(rs1.next()){
                txtBerat.setText(rs1.getString(3));
                if ("Pending".equals(rs1.getString(5))){
                    boxPending.setSelected(true);
                }else if("Checkout".equals(rs1.getString(5))){
                    boxOut.setSelected(true);
                }
                txtTotal.setText(rs1.getString(4));
                if("Laundry".equals(rs1.getString(2))){
                    comboLaundry.setSelectedIndex(1);
                }else{
                    comboLaundry.setSelectedIndex(2);
                }
            }
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
