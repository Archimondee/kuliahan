    public void hargaLaundry(){
        int pilihan = comboLaundry.getSelectedIndex();
        switch(pilihan){
            case 0:
                break;
            case 1:
                 harga = Integer.parseInt(txtBerat.getText());
                 harga = harga * 7000;
                 //JOptionPane.showMessageDialog(null, harga);
                 txtTotal.setText(String.valueOf(harga));
                 jenis_cucian = "Laundry";
                 break;
            case 2:
                harga = Integer.parseInt(txtBerat.getText()) * 10000;
                txtTotal.setText(String.valueOf(harga));
                jenis_cucian = "Laundry + Setrika";
        }
    }
    public void tambahData(){
        no_antrian = txtAntrian.getText();
        nama = txtNama.getText();
        berat_cucian = Integer.parseInt(txtBerat.getText());
        harga_cucian = Integer.parseInt(txtTotal.getText());
        //jenis_cucian;
        if(boxPending.isSelected() == true){
            status = "Pending";
        }else if(boxPending.isSelected() == false){
            status = "Checkout";
        }
        try{
            konekDatabase();
            Statement stmt = con.createStatement();
            String query = "INSERT INTO data_laundry VALUES ('"+no_antrian +"','"+nama+"')";
            String sql = "INSERT INTO detail_laundry VALUES ('"+ no_antrian +"',"
                    + "'"+jenis_cucian+"',"
                    + "'"+berat_cucian+"',"
                    + "'"+harga_cucian+"',"
                    + "'"+status+"')";
            stmt.executeUpdate(sql);
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(null, "Data berhasil dimasukan");
        }catch(SQLException | HeadlessException e){
            
        }
    }
